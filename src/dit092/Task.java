package dit092;

public class Task {
    private int startWeek;
    private int endWeek;

    public Task(int startWeek, int endWeek) {
        this.startWeek = startWeek;
        this.endWeek = endWeek;
    }

    public int getStartWeek() {
        return startWeek;
    }

    public int getEndWeek() { return endWeek; }

}
